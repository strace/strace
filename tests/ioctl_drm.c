	TEST_NULL_ARG(DRM_IOCTL_AGP_ENABLE);
	TEST_NULL_ARG(DRM_IOCTL_AGP_INFO);
	TEST_NULL_ARG(DRM_IOCTL_AGP_ALLOC);
